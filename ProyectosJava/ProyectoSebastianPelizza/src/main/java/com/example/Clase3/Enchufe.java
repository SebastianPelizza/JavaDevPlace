package com.example.Clase3;

public class Enchufe{
    private int patas;
    private String color;

    public Enchufe(int patas, String color) {
        this.patas = patas;
        this.color = color;
    }

    public int getPatas() {
        return patas;
    }

    public void setPatas(int patas) {
        this.patas = patas;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    
}